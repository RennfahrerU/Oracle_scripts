rem 9) Find out what is the date of twentieth anniversary for each employee.

--SELECT * FROM employees;

SELECT EMPLOYEE_ID AS ID, FIRST_NAME as FIRST_N, LAST_NAME AS LAST_N, HIRE_DATE AS HIRED, (SYSDATE - HIRE_DATE)/365 AS ACTUAL_YEARS, ADD_MONTHS(HIRE_DATE, 240) AS ANNIVERSARY
FROM EMPLOYEES
ORDER BY HIRED; --DESC